     private void loadItems(){
   
    try {
            String filePath = FilePath.itemPath;
            FileReader filereader = new FileReader(filePath);
            BufferedReader reader = new BufferedReader(filereader);
            String line;

            // Use a List to store selected data
            List<String> selectedData = new ArrayList<>();
            

            while ((line = reader.readLine()) != null) {
                String[] rowData = line.split(",");
                // Assuming rowData[0] is itemID, add it to the list
                selectedData.add(rowData[0]);
            }

            reader.close(); // Always close your resources
              jComboBox3.removeAllItems();
            // Loop through the collected data and print or process it
            for (String item : selectedData) {
                jComboBox3.addItem(item);
            }
            
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    private void loadSuppliers(){
        
         try {
            String filePath = FilePath.itemPath;
            FileReader filereader = new FileReader(filePath);
            BufferedReader reader = new BufferedReader(filereader);
            String line;

            // Use a List to store selected data
            List<String> selectedData = new ArrayList<>();
            

            while ((line = reader.readLine()) != null) {
                String[] rowData = line.split(",");
                if(rowData[0].equals(jComboBox3.getSelectedItem())){
                    selectedData.add(rowData[2]);
                }
            }

            reader.close(); // Always close your resources
              jComboBox1.removeAllItems();
            // Loop through the collected data and print or process it
            for (String item : selectedData) {
                jComboBox1.addItem(item);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    } 
