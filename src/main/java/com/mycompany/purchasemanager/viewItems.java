    public viewItems(String role) {
        tableModel = new DefaultTableModel(columnNames, 0);
        initComponents();
        this.role = role;
        items = new ArrayList<>();
        String filePath = FilePath.itemPath;
        Set<String> categories = item.loadItemsAndCategories(filePath, items);
        
        populateCategoryComboBox(categories);
        populateItemsTable(items);
    }

    private void populateCategoryComboBox(Set<String> categories) {
        jComboBoxCategory.removeAllItems();
        if (categories.isEmpty()) {
            jComboBoxCategory.addItem("No Categories Available");
        } else {
            jComboBoxCategory.addItem("All Categories");
            for (String category : categories) {
                jComboBoxCategory.addItem(category);
            }
        }
    }



    private void populateItemsTable(List<item> itemsToDisplay) {
        tableModel.setRowCount(0); 
        System.out.println("Populating table with " + itemsToDisplay.size() + " items");
        for (item item : itemsToDisplay) {
            tableModel.addRow(new Object[] {
                item.getItemCode(),
                item.getItemName(),
                item.getCategory(),
                item.getQuantityInStock(),
                item.getUnit(),
                item.getReorderLevel()
            });
        }
    }






