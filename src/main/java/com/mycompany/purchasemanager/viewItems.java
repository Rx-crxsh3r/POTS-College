    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        String selectedCategory = (String) jComboBoxCategory.getSelectedItem();
        if (selectedCategory == null) return; // Prevent null selection
        
        List<item> filteredItems = new ArrayList<>();
        if ("All Categories".equals(selectedCategory)) {
            filteredItems = items; 
        } else {
            filteredItems = item.filterByCategory(items, selectedCategory);
        }
        populateItemsTable(filteredItems);
    }//GEN-LAST:event_jButtonSearchActionPerformed

